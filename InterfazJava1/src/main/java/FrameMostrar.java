    public String[] generateRowEmp(Empleado emp) {
        String[] newRow = new String[6];
        newRow[0] = emp.getNombre();
        newRow[1] = emp.getApellido();
        newRow[2] = emp.getGenero();
        newRow[3] = emp.getFechaNac();
        newRow[4] = emp.getFechaIng();
        newRow[5] = Double.toString(emp.getSalario());
        return newRow;
    }

