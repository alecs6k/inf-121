    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        table.setRowCount(0);
        String nomArch = "empleados.dat";
        ObjectInputStream arch = null;
        try {
            arch = new ObjectInputStream(new FileInputStream(nomArch));
            while (true) {
                Empleado act = (Empleado)arch.readObject();
                table.addRow(generateRowEmp(act));
            }
        } catch (Exception e) {
            
        } finally {
            try {
                arch.close();
            } catch (IOException ex) {
                Logger.getLogger(FrameMostrar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

